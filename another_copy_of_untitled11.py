import numpy as np
import torch
import torch.nn as nn
import torch.nn.functional as F
import torch.optim as optim
from tqdm import tqdm
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
from sklearn.linear_model import LinearRegression

#loading the numpy arrays and downsampling
class BrainMRIDataset(torch.utils.data.Dataset):
    def __init__(self, file_paths, ages):
        self.file_paths = file_paths
        self.ages = ages

    def __len__(self): return len(self.file_paths)

    def __getitem__(self, idx):
        f, age = self.file_paths[idx], self.ages[idx]

        try:
            img = np.load(f)
        except:
            print(f"Could not load file: {f}")
            img = np.zeros((128, 128, 128))

        # Handle bad images
        if np.all(img == 0) or np.isnan(img).any() or np.isinf(img).any():
            img = np.zeros_like(img)

        # Safe normalization
        std = np.std(img)
        img = (img - np.mean(img)) / (std + 1e-5)
        img = np.nan_to_num(img, nan=0.0, posinf=0.0, neginf=0.0)

        # Convert to tensor & downsample
        img = torch.tensor(img, dtype=torch.float32).unsqueeze(0)  # [1, D, H, W]
        img = F.interpolate(img.unsqueeze(0), size=(64, 64, 64),
                            mode='trilinear', align_corners=False).squeeze(0)
        return img, torch.tensor(age, dtype=torch.float32)

# Defining 3D DenseNet-inspired Regression Model
class _DenseLayer3D(nn.Sequential):
    def __init__(self, num_input_features, growth_rate, bn_size, drop_rate):
        super().__init__()
        self.add_module('norm1', nn.BatchNorm3d(num_input_features))
        self.add_module('relu1', nn.ReLU(inplace=True))
        self.add_module('conv1', nn.Conv3d(num_input_features, bn_size * growth_rate,
                                           kernel_size=1, stride=1, bias=False))
        self.add_module('norm2', nn.BatchNorm3d(bn_size * growth_rate))
        self.add_module('relu2', nn.ReLU(inplace=True))
        self.add_module('conv2', nn.Conv3d(bn_size * growth_rate, growth_rate,
                                           kernel_size=3, stride=1, padding=1, bias=False))
        self.drop_rate = drop_rate

    def forward(self, x):
        new_features = super().forward(x)
        if self.drop_rate > 0:
            new_features = F.dropout(new_features, p=self.drop_rate, training=self.training)
        return torch.cat([x, new_features], 1)

class _DenseBlock3D(nn.Sequential):
    def __init__(self, num_layers, num_input_features, bn_size, growth_rate, drop_rate):
        super().__init__()
        for i in range(num_layers):
            layer = _DenseLayer3D(num_input_features + i * growth_rate, growth_rate,
                                  bn_size, drop_rate)
            self.add_module('denselayer%d' % (i + 1), layer)

class _TransitionLayer3D(nn.Sequential):
    def __init__(self, num_input_features, num_output_features):
        super().__init__()
        self.add_module('norm', nn.BatchNorm3d(num_input_features))
        self.add_module('relu', nn.ReLU(inplace=True))
        self.add_module('conv', nn.Conv3d(num_input_features, num_output_features,
                                           kernel_size=1, stride=1, bias=False))
        self.add_module('pool', nn.AvgPool3d(kernel_size=2, stride=2))

class BrainAge3DDenseNet(nn.Module):
    def __init__(self, growth_rate=32, block_config=(6, 12, 24, 16),
                 num_init_features=64, bn_size=4, drop_rate=0, num_classes=1):
        super().__init__()

        # Initial convolution
        self.features = nn.Sequential(
            nn.Conv3d(3, num_init_features, kernel_size=7, stride=2, padding=3, bias=False),
            nn.BatchNorm3d(num_init_features),
            nn.ReLU(inplace=True),
            nn.MaxPool3d(kernel_size=3, stride=2, padding=1)
        )

        # Each denseblock
        num_features = num_init_features
        for i, num_layers in enumerate(block_config):
            block = _DenseBlock3D(num_layers=num_layers, num_input_features=num_features,
                                 bn_size=bn_size, growth_rate=growth_rate, drop_rate=drop_rate)
            self.features.add_module('denseblock%d' % (i + 1), block)
            num_features = num_features + num_layers * growth_rate
            if i != len(block_config) - 1:
                trans = _TransitionLayer3D(num_input_features=num_features,
                                           num_output_features=num_features // 2)
                self.features.add_module('transition%d' % (i + 1), trans)
                num_features = num_features // 2

        # Final batch norm
        self.features.add_module('norm5', nn.BatchNorm3d(num_features))

        # Regression head
        self.regressor = nn.Sequential(
            nn.ReLU(inplace=True),
            nn.AdaptiveAvgPool3d((1, 1, 1)),
            nn.Flatten(),
            nn.Linear(num_features, num_classes)
        )

        # Initialize weights
        for m in self.modules():
            if isinstance(m, nn.Conv3d):
                nn.init.kaiming_normal_(m.weight)
            elif isinstance(m, nn.BatchNorm3d):
                nn.init.constant_(m.weight, 1)
                nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.Linear):
                nn.init.constant_(m.bias, 0)

    def forward(self, x):
        # Repeat single channel to 3 channels for initial convolution
        if x.shape[1] == 1:
            x = x.repeat(1, 3, 1, 1, 1)
        features = self.features(x)
        out = self.regressor(features)
        return out

#calculating hybrid loss
def hybrid_loss(preds, targets):
    return 0.5 * F.l1_loss(preds, targets) + 0.5 * F.mse_loss(preds, targets)

#defining model training
def train_model(model, train_loader, val_loader, device, epochs=40):
    optimizer = optim.AdamW(model.parameters(), lr=1e-4, weight_decay=1e-4)
    scheduler = optim.lr_scheduler.ReduceLROnPlateau(optimizer, mode='min', factor=0.3, patience=5)
    model.to(device)
    best_mae = float('inf')

    for epoch in range(epochs):
        model.train()
        total_loss = 0.0

        for X, y in tqdm(train_loader, desc=f"Epoch {epoch+1}/{epochs}"):
            X, y = X.to(device), y.to(device).unsqueeze(1)
            preds = model(X)

            if torch.isnan(preds).any() or torch.isnan(y).any():
                print("Skipping batch with NaN predictions or labels.")
                continue

            loss = hybrid_loss(preds, y)
            optimizer.zero_grad()
            loss.backward()
            optimizer.step()
            total_loss += loss.item()

        avg_train_loss = total_loss / (len(train_loader) + 1e-6)

        # --- Validation ---
        model.eval()
        val_preds, val_targets = [], []
        with torch.no_grad():
            for X, y in val_loader:
                X, y = X.to(device), y.to(device).unsqueeze(1)
                out = model(X)
                preds = out.cpu().numpy().flatten()
                targets = y.cpu().numpy().flatten()

                # Clean NaNs
                mask = ~np.isnan(preds) & ~np.isnan(targets)
                val_preds.extend(preds[mask])
                val_targets.extend(targets[mask])

        if len(val_preds) == 0:
            print("No valid validation samples.")
            continue

        val_mae = mean_absolute_error(val_targets, val_preds)
        val_rmse = np.sqrt(mean_squared_error(val_targets, val_preds))
        scheduler.step(val_mae)

        print(f"Epoch {epoch+1}/{epochs} | TrainLoss={avg_train_loss:.4f} | ValMAE={val_mae:.3f} | ValRMSE={val_rmse:.3f}")

        if val_mae < best_mae:
            best_mae = val_mae
            torch.save(model.state_dict(), "/content/drive/MyDrive/IXI-T1-Processed/best_brain_age_model.pth")

    print(f"\nBest Validation MAE: {best_mae:.3f}")

#Evaluation
def evaluate_model(model, test_loader, device):
    model.eval()
    preds, targets = [], []
    with torch.no_grad():
        for X, y in test_loader:
            X, y = X.to(device), y.to(device).unsqueeze(1)
            out = model(X)
            p = out.cpu().numpy().flatten()
            t = y.cpu().numpy().flatten()

            mask = ~np.isnan(p) & ~np.isnan(t)
            preds.extend(p[mask])
            targets.extend(t[mask])

    preds, targets = np.array(preds), np.array(targets)
    if len(preds) == 0:
        print("No valid samples for testing.")
        return

    lr = LinearRegression().fit(preds.reshape(-1, 1), targets)
    corrected = lr.predict(preds.reshape(-1, 1))

#calculating metrics
    mae = mean_absolute_error(targets, corrected)
    rmse = np.sqrt(mean_squared_error(targets, corrected))
    r2 = r2_score(targets, corrected)

    print(f"\nBrain Age Prediction:")
    print(f"MAE={mae:.2f} | RMSE={rmse:.2f} | R²={r2:.3f}")


import matplotlib.pyplot as plt
import seaborn as sns
import pandas as pd
import numpy as np
import torch
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

#loading best of the saved model
model_path = "/content/drive/MyDrive/IXI-T1-Processed/best_brain_age_model.pth"
device = "cuda" if torch.cuda.is_available() else "cpu"

model = BrainAge3DDenseNet().to(device) # Changed from BrainAge3DResNet to BrainAge3DDenseNet
model.load_state_dict(torch.load(model_path, map_location=device))
model.eval()
print(f" Loaded trained model from: {model_path}")

# -------------------------------
# Evaluate on test set
# -------------------------------
preds, targets = [], []

with torch.no_grad():
    for X, y in test_loader:
        X, y = X.to(device), y.to(device).unsqueeze(1)
        out = model(X)
        p = out.cpu().numpy().flatten()
        t = y.cpu().numpy().flatten()

        mask = ~np.isnan(p) & ~np.isnan(t)
        preds.extend(p[mask])
        targets.extend(t[mask])

preds, targets = np.array(preds), np.array(targets)

if len(preds) == 0:
    raise ValueError("No valid test samples found.")

# -------------------------------
# Apply Bias Correction
# -------------------------------
lr = LinearRegression().fit(preds.reshape(-1, 1), targets)
corrected_preds = lr.predict(preds.reshape(-1, 1))

mae = mean_absolute_error(targets, corrected_preds)
rmse = np.sqrt(mean_squared_error(targets, corrected_preds))
r2 = r2_score(targets, corrected_preds)
gap = corrected_preds - targets

print("\nFinal Test Results:")
print(f"MAE:  {mae:.2f} years")
print(f"RMSE: {rmse:.2f} years")
print(f"R²:   {r2:.3f}")
print(f"Mean Brain Age Gap: {np.mean(gap):.2f} \u00B1 {np.std(gap):.2f}")

# Visualization 1: Predicted vs Actual
plt.figure(figsize=(6,6))
sns.scatterplot(x=targets, y=corrected_preds, alpha=0.7)
plt.plot([min(targets), max(targets)], [min(targets), max(targets)], 'r--')
plt.xlabel("Actual Age")
plt.ylabel("Predicted Brain Age (corrected)")
plt.title("Brain Age Prediction: Actual vs Predicted")
plt.grid(True)
plt.show()

# Visualization 2: Brain Age Gap Distribution
plt.figure(figsize=(6,4))
sns.histplot(gap, bins=30, kde=True)
plt.axvline(np.mean(gap), color='r', linestyle='--', label=f"Mean = {np.mean(gap):.2f}")
plt.xlabel("Predicted - Actual Age (Years)")
plt.title("Brain Age Gap Distribution")
plt.legend()
plt.show()

# Save Predictions to Drive
results_df = pd.DataFrame({
    "actual_age": targets,
    "predicted_age": corrected_preds,
    "brain_age_gap": gap
})
results_path = "/content/drive/MyDrive/IXI-T1-Processed/test_predictions.csv"
results_df.to_csv(results_path, index=False)
print(f"Results saved to: {results_path}")
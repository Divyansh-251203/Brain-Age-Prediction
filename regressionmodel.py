# Basic imports
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

from sklearn.linear_model import LinearRegression
from sklearn.metrics import r2_score, mean_absolute_error

from google.colab import files

print("Please upload your CSV file (e.g., test_predictions_with_screentime.csv)")
uploaded = files.upload()

# Get the first uploaded file name
filename = list(uploaded.keys())[0]
print("Uploaded file:", filename)

# Read the uploaded CSV
df = pd.read_csv(filename)

# Show first few rows
df.head()

# Check for missing values in the key columns
print(df[['brain_age_gap', 'screen_time_hours']].isna().sum())

# Drop rows with missing BAG or screen time
df_clean = df.dropna(subset=['brain_age_gap', 'screen_time_hours']).copy()

print("Shape before:", df.shape)
print("Shape after cleaning:", df_clean.shape)

# Quick descriptive stats
df_clean[['brain_age_gap', 'screen_time_hours']].describe()

plt.figure(figsize=(6, 4))
plt.scatter(df_clean['screen_time_hours'], df_clean['brain_age_gap'])
plt.xlabel("Screen time (hours/day)")
plt.ylabel("Brain Age Gap (years)")
plt.title("Brain Age Gap vs Screen Time")
plt.grid(True)
plt.show()

X = df_clean[['screen_time_hours']].values
y = df_clean['brain_age_gap'].values

print("X shape:", X.shape)
print("y shape:", y.shape)

lin_reg = LinearRegression()
lin_reg.fit(X, y)

y_pred = lin_reg.predict(X)

# Metrics
r2 = r2_score(y, y_pred)
mae = mean_absolute_error(y, y_pred)

print("Slope (change in BAG per +1 hour/day):", lin_reg.coef_[0])
print("Intercept:", lin_reg.intercept_)
print("R²:", r2)
print("MAE:", mae)

plt.figure(figsize=(6, 4))

# Scatter of actual data
plt.scatter(df_clean['screen_time_hours'], df_clean['brain_age_gap'], alpha=0.7)

# Regression line
x_line = np.linspace(df_clean['screen_time_hours'].min(),
                     df_clean['screen_time_hours'].max(), 100).reshape(-1, 1)
y_line = lin_reg.predict(x_line)

plt.plot(x_line, y_line)  # regression line
plt.xlabel("Screen time (hours/day)")
plt.ylabel("Brain Age Gap (years)")
plt.title("Linear Regression: BAG vs Screen Time")
plt.grid(True)
plt.show()

# 1) Thresholds using medians
bag_median = df_clean['brain_age_gap'].median()
screen_median = df_clean['screen_time_hours'].median()

print("Median BAG:", bag_median)
print("Median screen time (hours/day):", screen_median)

# 2) Boolean flags
df_clean['high_bag'] = df_clean['brain_age_gap'] > bag_median
df_clean['high_screen'] = df_clean['screen_time_hours'] > screen_median

# 3) % relationship
high_screen_group = df_clean[df_clean['high_screen'] == True]

prop_high_bag_in_high_screen = high_screen_group['high_bag'].mean() * 100

print(f"Number of high screen-time cases: {len(high_screen_group)}")
print(f"Percentage of high screen-time cases with high BAG: {prop_high_bag_in_high_screen:.2f}%")